def rotLeft(a,d):
    size = len(a)
    new_array = ['' for _ in range(size)]
    if d%(size) == 0:
        return a
    else:
        d = d if d < size else d % size
        for i in range(size):
            new_array[i-d] = a[i]
        return new_array            


